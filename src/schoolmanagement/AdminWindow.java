    private void jMenuItem11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem11ActionPerformed
        // TODO add your handling code here:
        try{
            Vector<String> head=new Vector<String>();
            head.add("Student Id");
            head.add("Student Name");
            head.add("Department");
            head.add("Total Attandance");
            head.add("present");
            head.add("absent");
            DBConnection db=new DBConnection();
            db.pstmt=db.con.prepareStatement("select stid,stname,stdep from adstudent");
            db.rst=db.pstmt.executeQuery();
            Vector<Vector<String>> data=new Vector<Vector<String>>();
            
            while(db.rst.next()){
                Vector<String> temp=new Vector<String>();
                temp.add(db.rst.getString(1));
                String id=db.rst.getString(1);
                temp.add(db.rst.getString(2));
                temp.add(db.rst.getString(3));
                
                db.pstmt=db.con.prepareStatement("select count(atnd) from attend where stid=?");
                db.pstmt.setString(1, id);
                db.rst=db.pstmt.executeQuery();
                String atn="",pr="";
                if(db.rst.next()){
                    temp.add(db.rst.getString(1));
                    atn=db.rst.getString(1);
                }
                
                db.pstmt=db.con.prepareStatement("select count(atnd) from attend where stid='id' && atnd='p'");
                db.rst=db.pstmt.executeQuery();
                if(db.rst.next()){
                    temp.add(db.rst.getString(1));
                    pr=db.rst.getString(1);
                }
                String ab= Integer.parseInt(atn)-Integer.parseInt(pr)+"";
                temp.add(ab);
                data.add(temp);
            }
            jTable5.setModel(new DefaultTableModel(data,head));
            attandanceList.setVisible(true);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem11ActionPerformed

