    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        try{
            int id=0;
            String name=jTextField1.getText();
            String dep=jTextField2.getText();
            String cour=jTextField3.getText();
            String gme=jTextField4.getText();
            String mob=jTextField5.getText();
            String dob=jTextField6.getText();
            String mail=jTextField7.getText();
            String add=jTextField8.getText();
            String stat="";
            if(jRadioButton1.isSelected()==true){
                stat="due";
            }
            else if(jRadioButton2.isSelected()==true){
                stat="submit";
            }
            
            DBConnection db= new DBConnection();
            db.pstmt=db.con.prepareStatement("select max(stid) from adstudent");
            db.rst=db.pstmt.executeQuery();
            if(db.rst.next()){
                id=db.rst.getInt(1);
                id++;
            }
            db.pstmt=db.con.prepareStatement("insert into adstudent(stid,stname,stdep,stcour,gname,mob,dob,stmail,stadd,fee) values(?,?,?,?,?,?,?,?,?,?)");
            db.pstmt.setInt(1,id);
            db.pstmt.setString(2,name);
            db.pstmt.setString(3,dep);
            db.pstmt.setString(4,cour);
            db.pstmt.setString(5,gme);
            db.pstmt.setString(6,mob);
            db.pstmt.setString(7,dob);
            db.pstmt.setString(8,mail);
            db.pstmt.setString(9,add);
            db.pstmt.setString(10,stat);
            int i=db.pstmt.executeUpdate();
            if(i>0){
                JOptionPane.showMessageDialog(this,"Added Student Successfully");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextField8.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this,"Error in Adding of Student");
            }
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton13ActionPerformed

